async def _upsert_effects(db_path: str, effects: Iterable[tuple[str, str]], effect_type: str) -> None:
    async with aiosqlite.connect(db_path) as db:
        for button_name, prompt in effects:
            cur = await db.execute('SELECT id FROM effects WHERE button_name = ?', (button_name,))
            row = await cur.fetchone()
            if row:
                await db.execute(
                    'UPDATE effects SET prompt = ?, is_active = 1, type = ? WHERE id = ?',
                    (prompt, effect_type, row[0]),
                )
            else:
                cur_max = await db.execute('SELECT COALESCE(MAX(sort_order), 0) FROM effects')
                max_sort = (await cur_max.fetchone())[0]
                await db.execute(
                    'INSERT INTO effects (button_name, prompt, type, sort_order, created_at) '
                    'VALUES (?, ?, ?, ?, datetime("now"))',
                    (button_name, prompt, effect_type, int(max_sort) + 1),
                )
        await db.commit()


